var pomo=0;

function PomoPlus() {
    pomo++;
    document.getElementById("time").innerHTML = pomo;
}

function PomoMinus() {
    pomo--;
    document.getElementById("time").innerHTML = pomo;
}

function BreakPlus() {
    pomo++;
    document.getElementById("break").innerHTML = pomo;
}

function BreakMinus() {
    pomo--;
    document.getElementById("break").innerHTML = pomo;
}